let currentLang = 'ja';

function t(key) {
  return TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS['en']?.[key] || key;
}

function selectLang(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;

  // 言語選択画面を隠してチェックイン画面を表示
  document.getElementById('langSelect').classList.add('hidden');
  document.getElementById('checkinScreen').classList.remove('hidden');

  // 現在の言語表示を更新
  document.getElementById('currentLangFlag').textContent = TRANSLATIONS[lang].flag;
  document.getElementById('currentLangName').textContent = TRANSLATIONS[lang].name;

  // 全テキストを更新
  applyTranslations();
}

function showLangSelect() {
  document.getElementById('langSelect').classList.remove('hidden');
  document.getElementById('checkinScreen').classList.add('hidden');
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });
}
